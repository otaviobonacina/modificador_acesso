public class Filme {
    private int codigo;
    private String nome;
    private double valor;
    private boolean disonivel;
    public int getCodigo() {
        return codigo;
    }
    public void setCodigo(int codigo) {
        this.codigo = codigo;
    }
    public String getNome() {
        return nome;
    }
    public void setNome(String nome) {
        this.nome = nome;
    }
    public double getValor() {
        return valor;
    }
    public void setValor(double valor) {
        this.valor = valor;
    }
    public boolean isDisonivel() {
        return disonivel;
    }
    public void setDisonivel(boolean disonivel) {
        this.disonivel = disonivel;
    }
    
}
